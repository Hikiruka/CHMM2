#include <stdio.h>
#include <string.h>
#include <3ds.h>
#include "include/luaplayer.h"
#include "include/Graphics/Graphics.h"
#include "include/ftp/ftp.h"
#include "index.cpp"
#include "include/khax/khax.h"

const char *errMsg;
unsigned char *buffer;
char cur_dir[256];
char start_dir[256];
bool CIA_MODE;
bool is3DSX;

int main(int argc, char **argv)
{
	srvInit();	
	aptInit();
	gfxInitDefault();
	acInit();
	initCfgu();
	httpcInit();
	ptmInit();
	hidInit(NULL);
	irrstInit(NULL);
	aptOpenSession();
	Result ret=APT_SetAppCpuTimeLimit(NULL, 30);
	aptCloseSession();
	fsInit();
	hbInit();
	Handle fileHandle;
	u64 size;
	u32 bytesRead;
	int restore;
	
	if (argc > 0){
		is3DSX = true;
		khaxInit();
	}else is3DSX = false;
	
	while(aptMainLoop())
	{
		restore=0;		
		char error[256];
		char startstring[256];
		if (!is3DSX){
			while (aptGetStatus() != 0x04 or aptGetStatusPower() != 0x01){
				sprintf(startstring,"CHMM Controls:\n\nA = Install Theme\nY = Theme Preview\nSTART = Exit CHMM\n\nPress POWER to initialize CHMM.");
				RefreshScreen();
				ClearScreen(0);
				ClearScreen(1);
				DebugOutput(startstring);
				gfxFlushBuffers();
				gfxSwapBuffers();
				gspWaitForVBlank();
			}
		}
		
		errMsg = runScript((char*)index_lua, true);
		
		if (errMsg != NULL);
                {
				// Fake error to force interpreter shutdown
				if (strstr(errMsg, "lpp_exit_04")){
					break;
				}
				
				strcpy(error,"Error: ");
				strcat(error,errMsg);
				strcat(error,"\n\nPress A to restart\nPress B to exit");
				}

						while (restore==0){
							gspWaitForVBlank();
							RefreshScreen();
							ClearScreen(0);
							ClearScreen(1);
							DebugOutput(error);
							hidScanInput();
							if(hidKeysDown() & KEY_A){
								strcpy(cur_dir,start_dir);
								restore=1;
							}else if(hidKeysDown() & KEY_B){
								restore=2;
							}
							gfxFlushBuffers();
							gfxSwapBuffers();
						}
						if (restore==2){
							break;
						}
	}
	if (is3DSX) khaxExit;
	fsExit();
	irrstExit();
	hidExit();
	ptmExit();
	hbExit();
	acExit();
	exitCfgu();
	gfxExit();
	aptExit();
	srvExit();

	return 0;
}
